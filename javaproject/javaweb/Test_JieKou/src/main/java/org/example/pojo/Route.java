package org.example.pojo;

public class Route {
    int id;
    String shippingsite;
    String receivingstation;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getShippingsite() {
        return shippingsite;
    }

    public void setShippingsite(String shippingsite) {
        this.shippingsite = shippingsite;
    }

    public String getReceivingstation() {
        return receivingstation;
    }

    public void setReceivingstation(String receivingstation) {
        this.receivingstation = receivingstation;
    }
}
